package javaSample;

public class LgTv {
	public LgTv() {
		System.out.println("Lg TV 생성");
	}

	public void turnOn() {
		System.out.println("Lg TV를 켬");
	}
	public void turnOff() {
		System.out.println("Lg TV를 끔");
	}
	public void soundOn() {
		System.out.println("Lg 소리켬");
	}
	public void soundoff() {
		System.out.println("Lg 소리끔");
	}
}
